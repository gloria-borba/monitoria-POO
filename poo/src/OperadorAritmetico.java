public class OperadorAritmetico {
    public static void main(String[] args) {
        int a = 1;
        int b = 5;
        //Operadores aritméticos
        //Soma
        int soma = a + b;
        int soma1 = 2 + 2;
        System.out.println(soma);
        System.out.println(soma1);

        //Subtração
        int substracao = a - b;
        int substracao1 = 5 - 4;
        System.out.println(substracao);
        System.out.println(substracao1);

        //divisão
        int divisao = a / b;
        int divisao1 = 8 / 2;
        System.out.println(divisao);
        System.out.println(divisao1);

        //multiplicacao
        int multiplicacao = a * b;
        int multiplicacao1 = 5 * 3;
        System.out.println(multiplicacao);
        System.out.println(multiplicacao1);

        //resto da divisão
        //Usado bastante para saber se o número é par ou impar
        int restoDivisao = a % b;
        int restoDivisao1 = 8 % 2;
        System.out.println(restoDivisao);
        System.out.println(restoDivisao1);
    }
}
